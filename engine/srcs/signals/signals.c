#include "signals.h"

void cleanup_handler(int signum)
{
    // For the launcher to send the signal to the engine to exit cleanly
    if (signum == SIGNAL_CLEANUP && g_engine != NULL)
    {
        dr_exit(g_engine);
        print_memory_usage("EXIT");
        shutdown_memory();
        exit(EXIT_SUCCESS);
    }
}
